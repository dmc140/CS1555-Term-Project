DROP TABLE users CASCADE CONSTRAINTS;
DROP TABLE friendships CASCADE CONSTRAINTS;
DROP TABLE groups CASCADE CONSTRAINTS;
DROP TABLE members CASCADE CONSTRAINTS;
DROP TABLE messages CASCADE CONSTRAINTS;

CREATE TABLE users (
        user_ID INT PRIMARY KEY,
        name VARCHAR(32) NOT NULL,
        email VARCHAR(32),
        dob DATE NOT NULL,
        last_log TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE friendships (
        user_a INT,
        user_b INT,
        pending INT DEFAULT 1,
        date_add DATE,
        CONSTRAINT pend CHECK(pending = 0 OR pending = 1),
        CONSTRAINT fka FOREIGN KEY (user_a) REFERENCES users(user_ID),
        CONSTRAINT fkb FOREIGN KEY (user_b) REFERENCES users(user_ID)
);

CREATE TABLE groups (
        g_ID INT PRIMARY KEY,
        gname VARCHAR(64) NOT NULL,
        description VARCHAR(250),
        glimit INT NOT NULL
);

CREATE TABLE members (
        u_ID INT,
        g_ID INT,
        CONSTRAINT fku FOREIGN KEY (u_ID) REFERENCES users(user_ID),
        CONSTRAINT fkg FOREIGN KEY (g_ID) REFERENCES groups(g_ID)
);

CREATE TABLE messages (
	msg_ID INT PRIMARY KEY,
	subject VARCHAR(32),
	body VARCHAR(100),
	sender INT,
	date_sent DATE DEFAULT CURRENT_TIMESTAMP,
	r_ID INT NOT NULL,
	is_user INT NOT NULL,
	CONSTRAINT usercheck CHECK (is_user = 0 OR is_user = 1),
	CONSTRAINT send FOREIGN KEY (sender) REFERENCES users(user_ID)
);

CREATE OR REPLACE TRIGGER added
BEFORE UPDATE OF pending ON friendships
FOR EACH ROW
BEGIN
        :new.date_add := CURRENT_TIMESTAMP;
END;
/

CREATE OR REPLACE TRIGGER drop_user
BEFORE DELETE ON users
FOR EACH ROW
BEGIN 
        DELETE FROM members WHERE members.u_ID = :old.user_ID;
END;
/

